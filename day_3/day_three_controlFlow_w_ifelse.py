#Control Flow with if / else and Conditional Operators
# In this code, we will use if / else statements to control the flow of the program
# and make decisions based on certain conditions.

water_level = 50  # Example water level in cm
# Check if the water level is above or below a certain threshold

if water_level > 80:
    print("DRAIN THE WATER!")
else:
    print("Water level is acceptable.")


# Example of using if / else to check a condition
print("Welcome to Ben's Roller Coaster!")

# Get the user's height
height = int(input("Please enter your height in cm: "))

# Check if the height is above or below a certain threshold
if height >= 150:
    print("You are tall enough to ride the roller coaster!")
else:
    print("Sorry, you need to be taller to ride the roller coaster.")